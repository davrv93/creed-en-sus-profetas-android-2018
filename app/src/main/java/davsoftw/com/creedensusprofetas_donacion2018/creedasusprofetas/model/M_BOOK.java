package davsoftw.com.creedensusprofetas_donacion2018.creedasusprofetas.model;

public class M_BOOK {

    private String id;
    private String idbook;
    private String idtestament;
    private String name;
    private String abrev;
    private String language;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdbook() {
		return idbook;
	}
	public void setIdbook(String idbook) {
		this.idbook = idbook;
	}
	public String getIdtestament() {
		return idtestament;
	}
	public void setIdtestament(String idtestament) {
		this.idtestament = idtestament;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAbrev() {
		return abrev;
	}
	public void setAbrev(String abrev) {
		this.abrev = abrev;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	
}
